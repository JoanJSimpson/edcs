    private void botonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonModificarActionPerformed
        int exito=0;
        
        String titulo="", director="", genero="", argumento ="";
        //System.out.println("Fecha: "+txtFechaModificado.getText() + "Titulo: "+txtTituloModificado.getText());
        java.sql.Date sqlDate=null;
        titulo=txtTituloModificado.getText();
        
        director=txtDirectorModificado.getText();
        genero=txtGeneroModificado.getText();
        String fecha=txtFechaModificado.getText();
        if(fecha.equals("")){
            //System.out.println("Fecha: "+fecha);
        }else{
            SimpleDateFormat formatDate = new SimpleDateFormat("yyyy-MM-dd");
            java.util.Date invoiceDate = null;
            try {

                    invoiceDate = formatDate.parse(fecha);
            } catch (ParseException ex) {
                    Logger.getLogger(pelisVisualizar.class.getName()).log(Level.SEVERE, null, ex);
            }
            sqlDate = new java.sql.Date(invoiceDate.getTime());
            //System.out.println("sqlDate= "+sqlDate);
        }
        
        
        argumento=txtArgumentoModificado.getText();
        
        exito = metodosbd.ModificarPeliculas(txtBusquedaMod.getText(), titulo, director, genero, 
                sqlDate, argumento);
        
        if(exito>0){
            JOptionPane.showMessageDialog(null, "Los datos se han actualizado correctamente", 
                    "Exito en la operacion",JOptionPane.INFORMATION_MESSAGE);
            metodosbd.BuscarPeliculas(txtBusqueda.getText(), cboxFiltro.getSelectedItem().toString(), tablaDatos);
        }else{
            JOptionPane.showMessageDialog(null, "Los datos no se pudieron actualizar", 
                    "Error en la operacion",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botonModificarActionPerformed

