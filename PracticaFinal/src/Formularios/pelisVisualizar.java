    private void rellenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rellenarActionPerformed
        int fila=tablaDatos.getSelectedRow();
        if (fila==-1){
            JOptionPane.showMessageDialog(null,"Por favor, seleccione una fila"); 
        }else{
            //System.out.println("fila: "+fila);
            String id=(String)tablaDatos.getValueAt(fila, 0);
            //System.out.println("id: "+id);
            txtBusquedaMod.setText(id);
            String titulo=(String)tablaDatos.getValueAt(fila, 1);
            txtTituloModificado.setText(titulo);
            String director=(String)tablaDatos.getValueAt(fila, 2);
            txtDirectorModificado.setText(director);
            String genero=(String)tablaDatos.getValueAt(fila, 3);
            txtGeneroModificado.setText(genero);
            String fecha=(String)tablaDatos.getValueAt(fila, 4);
            txtFechaModificado.setText(fecha);
            String argumento=(String)tablaDatos.getValueAt(fila, 5);
            txtArgumentoModificado.setText(argumento);
        }
        
    }//GEN-LAST:event_rellenarActionPerformed

