    private void borrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_borrarActionPerformed
        int exito=0;
        exito = metodosbd.Borrar(txtBusquedaMod.getText(),"PELICULAS","Codigo_Pelicula");
        
        if(exito>0){
            JOptionPane.showMessageDialog(null, "Los datos se han borrado correctamente", 
                    "Exito en la operacion",JOptionPane.INFORMATION_MESSAGE);
            
            metodosbd.BuscarPeliculas(txtBusqueda.getText(), cboxFiltro.getSelectedItem().toString(), tablaDatos);
        }else{
            JOptionPane.showMessageDialog(null, "Los datos no se pudieron borrar", 
                    "Error en la operacion",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_borrarActionPerformed

